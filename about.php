<script>
    // FAQ Toggle Function
    function toggleFaq(index) {
        const items = document.querySelectorAll('.faq-item');
        items.forEach((item, i) => {
            if (i === index) {
                item.classList.toggle('active');
            } else {
                item.classList.remove('active');
            }
        });
    }
